package com.st.sm.entity;
/**
 * 学生
 * @author 不知不觉
 *
 */
public class Student {
	private String st_id; //学生编号
	private String st_name; //学生姓名
	private Integer st_sex; //学生性别
	private String st_image; //学生头像
	private String st_address; //家庭住址
	private String st_phone; //家庭联系方式
	private String st_hostel; //宿舍
	private String st_tc_id; //班主任
	private String st_class_id; //年级
	private Integer st_age; //年龄
	public String getSt_id() {
		return st_id;
	}
	public void setSt_id(String st_id) {
		this.st_id = st_id;
	}
	public String getSt_name() {
		return st_name;
	}
	public void setSt_name(String st_name) {
		this.st_name = st_name;
	}
	public Integer getSt_sex() {
		return st_sex;
	}
	public void setSt_sex(Integer st_sex) {
		this.st_sex = st_sex;
	}
	public String getSt_image() {
		return st_image;
	}
	public void setSt_image(String st_image) {
		this.st_image = st_image;
	}
	public String getSt_address() {
		return st_address;
	}
	public void setSt_address(String st_address) {
		this.st_address = st_address;
	}
	public String getSt_phone() {
		return st_phone;
	}
	public void setSt_phone(String st_phone) {
		this.st_phone = st_phone;
	}
	public String getSt_hostel() {
		return st_hostel;
	}
	public void setSt_hostel(String st_hostel) {
		this.st_hostel = st_hostel;
	}
	public String getSt_tc_id() {
		return st_tc_id;
	}
	public void setSt_tc_id(String st_tc_id) {
		this.st_tc_id = st_tc_id;
	}
	public String getSt_class_id() {
		return st_class_id;
	}
	public void setSt_class_id(String st_class_id) {
		this.st_class_id = st_class_id;
	}
	public Integer getSt_age() {
		return st_age;
	}
	public void setSt_age(Integer st_age) {
		this.st_age = st_age;
	}
	
	
}
