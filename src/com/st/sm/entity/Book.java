public class Book {
	private String bk_id;
	private String bk_class_id;
	private String bk_name;
	private String bk_author;
	private String bk_address;
	private String bk_summary;
	private String bk_image;
	public String getBk_id() {
		return bk_id;
	}
	public void setBk_id(String bk_id) {
		this.bk_id = bk_id;
	}
	public String getBk_class_id() {
		return bk_class_id;
	}
	public void setBk_class_id(String bk_class_id) {
		this.bk_class_id = bk_class_id;
	}
	public String getBk_name() {
		return bk_name;
	}
	public void setBk_name(String bk_name) {
		this.bk_name = bk_name;
	}
	public String getBk_author() {
		return bk_author;
	}
	public void setBk_author(String bk_author) {
		this.bk_author = bk_author;
	}
	public String getBk_address() {
		return bk_address;
	}
	public void setBk_address(String bk_address) {
		this.bk_address = bk_address;
	}
	public String getBk_summary() {
		return bk_summary;
	}
	public void setBk_summary(String bk_summary) {
		this.bk_summary = bk_summary;
	}
	public String getBk_image() {
		return bk_image;
	}
	public void setBk_image(String bk_image) {
		this.bk_image = bk_image;
	}
	

}
