package com.st.sm.entity;

import java.util.Date;

public class Study {
	private String stu_Id;
	private String stu_Title;
	private String stu_Content;
	private Date stu_Date;
	private String stu_Type;
	private String stu_Age;
	public String getStu_Id() {
		return stu_Id;
	}
	public void setStu_Id(String stu_Id) {
		this.stu_Id = stu_Id;
	}
	public String getStu_Title() {
		return stu_Title;
	}
	public void setStu_Title(String stu_Title) {
		this.stu_Title = stu_Title;
	}
	public String getStu_Content() {
		return stu_Content;
	}
	public void setStu_Content(String stu_Content) {
		this.stu_Content = stu_Content;
	}
	public Date getStu_Date() {
		return stu_Date;
	}
	public void setStu_Date(Date stu_Date) {
		this.stu_Date = stu_Date;
	}
	public String getStu_Type() {
		return stu_Type;
	}
	public void setStu_Type(String stu_Type) {
		this.stu_Type = stu_Type;
	}
	public String getStu_Age() {
		return stu_Age;
	}
	public void setStu_Age(String stu_Age) {
		this.stu_Age = stu_Age;
	}
	
}
